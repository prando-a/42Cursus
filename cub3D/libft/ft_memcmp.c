int	ft_memcmp(const void *s1, const void *s2, unsigned int n)
{
	unsigned char		*p1;
	unsigned char		*p2;

	p1 = (unsigned char *)s1;
	p2 = (unsigned char *)s2;
	while (n--)
	{
		if (*p1 != *p2)
			return (*p1 - *p2);
		p1++;
		p2++;
	}
	return (0);
}
