#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	size_t	ln;

	ln = n;
	if (n < 0)
	{
		ft_putchar_fd('-', fd);
		ln = -ln;
	}
	if (ln >= 10)
	{
		ft_putnbr_fd(ln / 10, fd);
		ft_putnbr_fd(ln % 10, fd);
	}
	else
	{
		ft_putchar_fd(ln + '0', fd);
	}
}
