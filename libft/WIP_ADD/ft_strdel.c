#include "libft.h"

void	ft_strdel(char **as)
{
	int	i;

	i = 0;
	while (as[i])
		free(as[i++]);
	free(as);
	as = NULL;
}

void	*ft_memalloc(size_t size)
{
	void	*mem;

	if (!(mem = malloc(size)))
		return (NULL);
	ft_bzero(mem, size);
	return (mem);
}