#include "libft.h"

void	ft_putendl_fd(char *s, int fd);

void	ft_msg(char *str)
{
	ft_putendl_fd(str, 1);
}

void	ft_err_msg(char	*str)
{
	ft_putendl_fd(str, 2);
}

void	ft_putendl_fd(char *s, int fd)
{
	write(fd, s, ft_strlen(s));
	write(fd, "\n", 1);
}
